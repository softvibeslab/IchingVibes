def get_hexagram(number: int):
    """Obtener hexagrama por número"""
    return ICHING_HEXAGRAMS.get(number)

def calculate_hexagram_from_lines(lines: list) -> int:
    """Calcular número de hexagrama desde lista de líneas (0 o 1)"""
    # Convertir líneas a número binario
    binary_str = ''.join(str(line) for line in lines)
    
    # Buscar hexagrama que coincida
    for num, hexagram in ICHING_HEXAGRAMS.items():
        hex_lines = ''.join(str(line) for line in hexagram['lines'])
        if hex_lines == binary_str:
            return num
    
    return 1  # Default al Creativo si no se encuentra
